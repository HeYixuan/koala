package org.igetwell.wechat.sdk;

public class WechatUser {
    /**
     * 普通用户的标识
     */
    private String openId;
    /**
     * 普通用户昵称
     */
    private String nickname;
    /**
     * 用户头像
     */
    private String headimgurl;
    /**
     * 用户统一标识。针对一个微信开放平台帐号下的应用，同一用户的unionid是唯一的。
     */
    private String unionid;
}
